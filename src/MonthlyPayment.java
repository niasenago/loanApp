class MonthlyPayment {
    double paskoloslikutis;
    double mokejimoSuma;
    double paskolosDalis;
    double palukanuDalis;
    MonthlyPayment(double paskoloslikutisIn,double mokejimoSumaIn,double paskolosDalisIn,double palukanuDalisIn ){
        paskoloslikutis = paskoloslikutisIn;
        mokejimoSuma = mokejimoSumaIn;
        paskolosDalis = paskolosDalisIn;
        palukanuDalis = palukanuDalisIn;
    }

}
